public class ReadJob extends Job {

    // Additional detailed attributes
    private String experience;
    private String qualifications;
    private String jobPostingDate;
    private String preference;
    private String contactPerson;
    private String contact;
    private String role;
    private String jobPortal;
    private String jobDescription;
    private String benefits;
    private String skills;
    private String responsibilities;
    private String companyProfileJson;

    // Getters and setters for detailed attributes
    public String getExperience() { return experience; }
    public void setExperience(String experience) { this.experience = experience; }

    public String getQualifications() { return qualifications; }
    public void setQualifications(String qualifications) { this.qualifications = qualifications; }

    public String getJobPostingDate() { return jobPostingDate; }
    public void setJobPostingDate(String jobPostingDate) { this.jobPostingDate = jobPostingDate; }

    public String getPreference() { return preference; }
    public void setPreference(String preference) { this.preference = preference; }

    public String getContactPerson() { return contactPerson; }
    public void setContactPerson(String contactPerson) { this.contactPerson = contactPerson; }

    public String getContact() { return contact; }
    public void setContact(String contact) { this.contact = contact; }

    public String getRole() { return role; }
    public void setRole(String role) { this.role = role; }

    public String getJobPortal() { return jobPortal; }
    public void setJobPortal(String jobPortal) { this.jobPortal = jobPortal; }

    public String getJobDescription() { return jobDescription; }
    public void setJobDescription(String jobDescription) { this.jobDescription = jobDescription; }

    public String getBenefits() { return benefits; }
    public void setBenefits(String benefits) { this.benefits = benefits; }

    public String getSkills() { return skills; }
    public void setSkills(String skills) { this.skills = skills; }

    public String getResponsibilities() { return responsibilities; }
    public void setResponsibilities(String responsibilities) { this.responsibilities = responsibilities; }

    public String getCompanyProfileJson() { return companyProfileJson; }
    public void setCompanyProfileJson(String companyProfileJson) { this.companyProfileJson = companyProfileJson; }
}
